package application;


public class Venue {

	private String venueName;
	private String venueAddressLineOne;
	private String venueAddressLineTwo;
	private String venueTownCity;
	private String venuePostcode;
	private String venueEmail;
	private String venueTel;
	
	//getters
	public String getVenueName(){ return venueName; }
	public String getVenueAddressLineOne(){ return venueAddressLineOne; }
	public String getVenueAddressLineTwo(){ return venueAddressLineTwo; }
	public String getVenueTownCity(){ return venueTownCity; }
	public String getVenuePostcode(){ return venuePostcode; }
	public String getVenueTel(){ return venueTel; }
	public String getVenueEmail(){ return venueEmail; }
	
	//setters
	public void setVenueName(String venueName){ this.venueName = venueName; }
	public void setVenueAddressLineOne(String venueAddressLineOne){ this.venueAddressLineOne = venueAddressLineOne; }
	public void setVenueAddressLineTwo(String venueAddressLineTwo){ this.venueAddressLineTwo = venueAddressLineTwo; }
	public void setVenueTownCity(String venueTownCity){ this.venueTownCity = venueTownCity; }
	public void setVenuePostcode(String venuePostcode){ this.venuePostcode = venuePostcode; }
	public void setVenueTel(String venueTel){ this.venueTel = venueTel; }
	public void setVenueEmail(String venueEmail){ this.venueEmail = venueEmail; }
	
	
	
	
}
